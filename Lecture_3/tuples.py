# Tuples
# A built-in data type that lets us create immutable sequnece of values
tup=(87,99,29,46,77)
# tup[0]=66 ->>> This is not allowed becuase tuples are immutable

# Tuple methods
print(tup.index(99)) # returns index of first occurance
print(tup.count(2))  # count the total occurances
print(len(tup))
